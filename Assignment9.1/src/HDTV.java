
public class HDTV {
	private int size;
	private String brand;
	
	public HDTV(int size,String brand)
	{
		this.size = size;
		this.brand = brand;
		
	}
	
	public int getSize()
	{
		return size;
	}
	public void setSize()
	{
		this.size=size;
	}
	public String getBrand()
	{
		return brand;
	}
	public void setBrand()
	{
		this.brand=brand;
	}

}
